// Create the path new as a link to the same inode as old.
uint64
sys_link(void)
{
  char name[DIRSIZ], new[MAXPATH], old[MAXPATH];
  struct inode *dp, *ip;

  if(argstr(0, old, MAXPATH) < 0 || argstr(1, new, MAXPATH) < 0)
    return -1;

  begin_op();
  if((ip = namei(old)) == 0){
    end_op();
    return -1;
  }

  ilock(ip);
  if(ip->type == T_DIR){
    iunlockput(ip);
    end_op();
    return -1;
  }

  ip->nlink++;
  iupdate(ip);
  iunlock(ip);

  if((dp = nameiparent(new, name)) == 0)
    goto bad;
  ilock(dp);
  if(dp->dev != ip->dev || dirlink(dp, name, ip->inum) < 0){
    iunlockput(dp);
    goto bad;
  }
  iunlockput(dp);
  iput(ip);

  end_op();

  return 0;

bad:
  ilock(ip);
  ip->nlink--;
  iupdate(ip);
  iunlockput(ip);
  end_op();
  return -1;
}

// Is the directory dp empty except for "." and ".." ?
static int
isdirempty(struct inode *dp)
{
  int off;
  struct dirent de;

  for(off=2*sizeof(de); off<dp->size; off+=sizeof(de)){
    if(readi(dp, 0, (uint64)&de, off, sizeof(de)) != sizeof(de))
      panic("isdirempty: readi");
    if(de.inum != 0)
      return 0;
  }
  return 1;
}

uint64
sys_unlink(void)
{
  struct inode *ip, *dp;
  struct dirent de;
  char name[DIRSIZ], path[MAXPATH];
  uint off;

  if(argstr(0, path, MAXPATH) < 0)
    return -1;

  begin_op();
  if((dp = nameiparent(path, name)) == 0){
    end_op();
    return -1;
  }

  ilock(dp);

  // Cannot unlink "." or "..".
  if(namecmp(name, ".") == 0 || namecmp(name, "..") == 0)
    goto bad;

  if((ip = dirlookup(dp, name, &off)) == 0)
    goto bad;
  ilock(ip);

  if(ip->nlink < 1)
    panic("unlink: nlink < 1");
  if(ip->type == T_DIR && !isdirempty(ip)){
    iunlockput(ip);
    goto bad;
  }

  memset(&de, 0, sizeof(de));
  if(writei(dp, 0, (uint64)&de, off, sizeof(de)) != sizeof(de))
    panic("unlink: writei");
  if(ip->type == T_DIR){
    dp->nlink--;
    iupdate(dp);
  }
  iunlockput(dp);

  ip->nlink--;
  iupdate(ip);
  iunlockput(ip);

  end_op();

  return 0;

bad:
  iunlockput(dp);
  end_op();
  return -1;
}

static struct inode*
create(char *path, short type, short major, short minor)
{
  struct inode *ip, *dp;
  char name[DIRSIZ];

  if((dp = nameiparent(path, name)) == 0)
    return 0;

  ilock(dp);

  if((ip = dirlookup(dp, name, 0)) != 0){
    iunlockput(dp);
    ilock(ip);
    if(type == T_FILE && (ip->type == T_FILE || ip->type == T_DEVICE))
      return ip;
    iunlockput(ip);
    return 0;
  }

  if((ip = ialloc(dp->dev, type)) == 0)
    panic("create: ialloc");

  ilock(ip);
  ip->major = major;
  ip->minor = minor;
  ip->nlink = 1;
  iupdate(ip);

  if(type == T_DIR){  // Create . and .. entries.
    dp->nlink++;  // for ".."
    iupdate(dp);
    // No ip->nlink++ for ".": avoid cyclic ref count.
    if(dirlink(ip, ".", ip->inum) < 0 || dirlink(ip, "..", dp->inum) < 0)
      panic("create dots");
  }

  if(dirlink(dp, name, ip->inum) < 0)
    panic("create: dirlink");

  iunlockput(dp);

  return ip;
}

uint64
sys_open(void)
{
  char path[MAXPATH];
  int fd, omode;
  struct file *f;
  struct inode *ip;
  int n;

  if((n = argstr(0, path, MAXPATH)) < 0 || argint(1, &omode) < 0)
    return -1;

  begin_op();

  if(omode & O_CREATE){
    ip = create(path, T_FILE, 0, 0);
    if(ip == 0){
      end_op();
      return -1;
    }
  } else {
    if((ip = namei(path)) == 0){
      end_op();
      return -1;
    }
    ilock(ip);
    if(ip->type == T_DIR && omode != O_RDONLY){
      iunlockput(ip);
      end_op();
      return -1;
    }
  }

  if(ip->type == T_DEVICE && (ip->major < 0 || ip->major >= NDEV)){
    iunlockput(ip);
    end_op();
    return -1;
  }

  if((f = filealloc()) == 0 || (fd = fdalloc(f)) < 0){
    if(f)
      fileclose(f);
    iunlockput(ip);
    end_op();
    return -1;
  }

  if(ip->type == T_DEVICE){
    f->type = FD_DEVICE;
    f->major = ip->major;
  } else {
    f->type = FD_INODE;
    f->off = 0;
  }
  f->ip = ip;
  f->readable = !(omode & O_WRONLY);
  f->writable = (omode & O_WRONLY) || (omode & O_RDWR);

  if((omode & O_TRUNC) && ip->type == T_FILE){
    itrunc(ip);
  }

  iunlock(ip);
  end_op();

  return fd;
}

uint64
sys_mkdir(void)
{
  char path[MAXPATH];
  struct inode *ip;

  begin_op();
  if(argstr(0, path, MAXPATH) < 0 || (ip = create(path, T_DIR, 0, 0)) == 0){
    end_op();
    return -1;
  }
  iunlockput(ip);
  end_op();
  return 0;
}

uint64
sys_mknod(void)
{
  struct inode *ip;
  char path[MAXPATH];
  int major, minor;

  begin_op();
  if((argstr(0, path, MAXPATH)) < 0 ||
     argint(1, &major) < 0 ||
     argint(2, &minor) < 0 ||
     (ip = create(path, T_DEVICE, major, minor)) == 0){
    end_op();
    return -1;
  }
  iunlockput(ip);
  end_op();
  return 0;
}

uint64
sys_chdir(void)
{
  char path[MAXPATH];
  struct inode *ip;
  struct proc *p = myproc();
  
  begin_op();
  if(argstr(0, path, MAXPATH) < 0 || (ip = namei(path)) == 0){
    end_op();
    return -1;
  }
  ilock(ip);
  if(ip->type != T_DIR){
    iunlockput(ip);
    end_op();
    return -1;
  }
  iunlock(ip);
  iput(p->cwd);
  end_op();
  p->cwd = ip;
  return 0;
}

uint64
sys_exec(void)
{
  char path[MAXPATH], *argv[MAXARG];
  int i;
  uint64 uargv, uarg;

  if(argstr(0, path, MAXPATH) < 0 || argaddr(1, &uargv) < 0){
    return -1;
  }
  memset(argv, 0, sizeof(argv));
  for(i=0;; i++){
    if(i >= NELEM(argv)){
      goto bad;
    }
    if(fetchaddr(uargv+sizeof(uint64)*i, (uint64*)&uarg) < 0){
      goto bad;
    }
    if(uarg == 0){
      argv[i] = 0;
      break;
    }
    argv[i] = kalloc();
    if(argv[i] == 0)
      goto bad;
    if(fetchstr(uarg, argv[i], PGSIZE) < 0)
      goto bad;
  }

  int ret = exec(path, argv);

  for(i = 0; i < NELEM(argv) && argv[i] != 0; i++)
    kfree(argv[i]);

  return ret;

 bad:
  for(i = 0; i < NELEM(argv) && argv[i] != 0; i++)
    kfree(argv[i]);
  return -1;
}

uint64
sys_pipe(void)
{
  uint64 fdarray; // user pointer to array of two integers
  struct file *rf, *wf;
  int fd0, fd1;
  struct proc *p = myproc();

  if(argaddr(0, &fdarray) < 0)
    return -1;
  if(pipealloc(&rf, &wf) < 0)
    return -1;
  fd0 = -1;
  if((fd0 = fdalloc(rf)) < 0 || (fd1 = fdalloc(wf)) < 0){
    if(fd0 >= 0)
      p->ofile[fd0] = 0;
    fileclose(rf);
    fileclose(wf);
    return -1;
  }
  if(copyout(p->pagetable, fdarray, (char*)&fd0, sizeof(fd0)) < 0 ||
     copyout(p->pagetable, fdarray+sizeof(fd0), (char *)&fd1, sizeof(fd1)) < 0){
    p->ofile[fd0] = 0;
    p->ofile[fd1] = 0;
    fileclose(rf);
    fileclose(wf);
    return -1;
  }
  return 0;
}

uint64
sys_mmap(void) {
  uint64 addr, len;
  int prot, fd, flags, offset;
  struct file *f;

  if(argaddr(0, &addr) < 0 || argaddr(1, &len) < 0 || argint(2, &prot) < 0 || argint(3, &flags) < 0 
    || argfd(4, &fd, &f) < 0 || argint(5, &offset) < 0) 
    {
      // printf("failed 1!\n");
      return -1;
    }
    
  
  // printf("port = %d, flags = %d\n", port, flags);
  // check read only;

    if((!f->readable && (prot & PROT_READ)) || (!f->writable && (prot & PROT_WRITE) && (flags & MAP_SHARED))) {
      // printf("failed 2!\n");
      return -1;
    }
      
  
  struct proc *p = myproc();
  if(p->sz + len > MAXVA)
    panic("out of the max virtual address!");
  
  addr = p->sz;
  p->sz = p->sz + len;
  if(p->vma_cnt + 1>= NVMA)
    panic("mmap!");
  
  filedup(f);

  // vma_init(&p->vma_[p->vma_cnt]);
  p->vma_[p->vma_cnt].used = 1;
  p->vma_[p->vma_cnt].addr = addr;   // 这里不方便封装;
  p->vma_[p->vma_cnt].length = len;
  p->vma_[p->vma_cnt].prot = prot;
  p->vma_[p->vma_cnt].offset = offset;
  p->vma_[p->vma_cnt].flags = flags;
  p->vma_[p->vma_cnt].fd = fd;
  p->vma_[p->vma_cnt].f = f; 
  p->vma_[p->vma_cnt].used = 1;
  p->vma_cnt++;  

  return addr;
}

uint64
sys_munmap(void) {
  uint64 addr, len;
  if(argaddr(0, &addr) < 0 || argaddr(1, &len) < 0) {
    return -1;
  } 
  // 根据地址范围找到vma_id, 并判断是否在中间"打洞";
  // 如果该地址范围被修改, 写回;
  // 如果vma被全删除, 标记清除该vma, 并减少文件引用;
  // 使用uvmunmap删除地址的映射(不释放物理地址);
  // 成功返回0, 失败返回-1; 
  int vma_id = -1;
  struct proc *p = myproc();
  uint64 unmap_start = addr, unmap_end = addr + len;
  // printf("unmap_start: %p, unmap_end: %p\n", unmap_start, unmap_end);

  uint64 vma_start = 0, vma_end = 0;
  for(int i = 0; i < p->vma_cnt; i++) {
    //if(p->vma_[i] == 0)
      //continue;
    
    vma_start = p->vma_[i].addr;
    vma_end = p->vma_[i].addr + p->vma_[i].length;
    // printf("vma_start: %p vma_end: %p\n", vma_start, vma_end);
    if(unmap_start >= vma_start && unmap_end <= vma_end) {
      vma_id = i;
      break;
    }
  }
  
  if(vma_id == -1)
      panic("not found munmap area!\n");
  
  if(unmap_start > vma_start && unmap_end < vma_end) 
      panic("unmap between the vma!\n");
  
  // 先回收!
  //printf("unmap_start: %p, unmap_end: %p\n", unmap_start, unmap_end);

  struct vma vma_current = p->vma_[vma_id];
  struct file *f = vma_current.f;
  
  // filewrite(vma_current.f, addr, len);   // 先写回文件, 这里有问题, 不能直接用filewrite;
  if(vma_current.f->writable == 0 && vma_current.flags & MAP_SHARED) {
    printf("cannot write back!\n");
    return -1;
  }

  // 改变vma的addr, len, offset;
  int write_offset = vma_current.offset;
  if(unmap_start == vma_start) {
    vma_current.addr = unmap_end;
    vma_current.offset += len;
  }
  else if(unmap_end == vma_end) 
    write_offset = vma_current.offset + (vma_current.length - len);
  vma_current.length = vma_current.length - len;
  // 写回文件 
  if(vma_current.flags & MAP_SHARED) {
    // int r = 0;
    begin_op();
    ilock(f->ip);
    writei(f->ip, 1, addr, write_offset, len);
      // f->off += r;
    iunlock(f->ip);
    end_op();  
    // filewrite(f, addr, len);   // 这里可以改变文件偏移;
  }
  uvmunmap(p->pagetable, addr, len / PGSIZE, 1);   // 这里是否收回物理地址?
  if(vma_current.length == 0) {
    fileclose(vma_current.f);
    memset(&p->vma_[vma_id], 0, sizeof(p->vma_[vma_id]));
  }

  return 0;
}